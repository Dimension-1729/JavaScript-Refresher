//Modulus or Absolute
console.log(Math.abs(-4));      //4

//Round off
console.log(Math.round(4.6));   //5

//Round off to upper level(or higher number)
console.log(Math.ceil(4.1));    //5

//Round off to lower number
console.log(Math.floor(4.9));   //4

//power
console.log(Math.pow(2,3));

//Square-root
console.log(Math.sqrt(125));

//To find minimum or maximum value from array
console.log(Math.min(2,1,0,5,9));

//For Random value bw 0-1
console.log(Math.floor(Math.random()*10));  //0-1 value is mutliplied to get result into 10's and then float is removed using lower value

//To get a random value each time bw 100-200
console.log(Math.floor((Math.random() * 100) +1)+100);
